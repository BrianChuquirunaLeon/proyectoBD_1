    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        PreparedStatement ps=null;
        Conexion conn= new Conexion();
        Connection con = conn.getConexion();
        
        String sql = "INSERT INTO vendedor(nombre,apellido_mat,apellido_pat) VALUES (?,?,?)";
        
        try{
            ps= con.prepareStatement(sql);
            ps.setString(1,txtNombre.getText());
            ps.setString(2,txtApeMat.getText());
            ps.setString(3,txtxApePat.getText());
            
            ps.execute();// ejecuta el codigo sql guardado en ps y no devuelve ningun valor
            
            
        }catch(SQLException e){
            System.err.println(e);
            
        }
        finally{
            try{
                con.close();
            }catch(SQLException e){
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

