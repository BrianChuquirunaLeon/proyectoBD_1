    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ResultSet rs=null;
        PreparedStatement ps=null;
        Conexion conn= new Conexion();
        Connection con = conn.getConexion();
        String sql = "SELECT * FROM vendedor WHERE id_vendedor = ? ";
        
        try{
            ps= con.prepareStatement(sql);
            ps.setString(1,txtIdVendedor.getText());
            System.out.println(sql);
            
            rs = ps.executeQuery();// ejecuta el codigo sql guardado en ps y y devuelve los que hay dentro de un registro de la base de datos
            
            if(rs.next()){
                
                ps= con.prepareStatement(sql);
                txtNombre.setText(rs.getString("nombre"));
                txtApeMat.setText(rs.getString("apellido_mat"));
                txtxApePat.setText(rs.getString("apellido_pat"));
                ps.execute();// ejecuta el codigo sql guardado en ps y no devuelve ningun valor
                
            }
           
            
        }catch(SQLException e){
            System.err.println(e);
            
        }
        finally{
            try{
                con.close();
            }catch(SQLException e){
                System.err.println(e);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

